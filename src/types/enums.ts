/**
 * Enum representing various font sizes.
 * Each font size is mapped to a corresponding CSS class name.
 *
 * @enum {string}
 * @property {string} text-xs - Extra small text size.
 * @property {string} text-sm - Small text size.
 * @property {string} text-md - Medium text size.
 * @property {string} text-lg - Large text size.
 * @property {string} text-xl - Extra large text size.
 * @property {string} text-2xl - 2x extra large text size.
 * @property {string} text-3xl - 3x extra large text size.
 * @property {string} text-4xl - 4x extra large text size.
 * @property {string} text-5xl - 5x extra large text size.
 * @property {string} text-6xl - 6x extra large text size.
 */
export enum EFontSize {
    'text-xs' = 'text-xs',
    'text-sm' = 'text-sm',
    'text-md' = 'text-md',
    'text-lg' = 'text-lg',
    'text-xl' = 'text-xl',
    'text-2xl' = 'text-2xl',
    'text-3xl' = 'text-3xl',
    'text-4xl' = 'text-4xl',
    'text-5xl' = 'text-5xl',
    'text-6xl' = 'text-6xl',
}
